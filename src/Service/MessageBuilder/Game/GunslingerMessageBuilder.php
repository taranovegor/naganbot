namespace App\Service\MessageBuilder\Game;

use App\Entity\Game\Gunslinger;
use Twig\Environment;
use Twig\Error\LoaderError;
use Twig\Error\RuntimeError;
use Twig\Error\SyntaxError;

/**
 * Class GunslingerMessageBuilder
 */
class GunslingerMessageBuilder
{
    private Environment $twig;

    /**
     * GunslingerMessageBuilder constructor.
     *
     * @param Environment $twig
     */
    public function __construct(Environment $twig)
    {
        $this->twig = $twig;
    }

    /**
     * @return string
     *
     * @throws LoaderError
     * @throws RuntimeError
     * @throws SyntaxError
     */
    public function buildJoin(): string
    {
        return $this->twig->render('Game/Gunslinger/join.md.twig');
    }

    /**
     * @param Gunslinger $gunslinger
     *
     * @return string
     *
     * @throws LoaderError
     * @throws RuntimeError
     * @throws SyntaxError
     */
    public function buildShotHimself(Gunslinger $gunslinger): string
    {
        return $this->twig->render('Game/Gunslinger/shot_himself.md.twig', [
            'gunslinger' => $gunslinger,
        ]);
    }
}
